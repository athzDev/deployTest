function geti18n(lang: string = 'en-us', prefix = new Date().toISOString(), brandId: number): Promise<any> | undefined {
	const baseUrl = ''
	const url = ''
	// const langSelected = cookieUtil.get(ECookieName.COOKIE_LANG) || 'en-us'
    const langSelected = 'en-us'
    
	if (util.isClient) {
		if (!(global as any).langObj[lang]) {
			const baseData: any = geti18nClient(langSelected);
			const data: any = geti18nClient(langSelected);
			(global as any).langObj[lang] = {
				...baseData,
				...Object.keys(data).reduce((result: any, key: string) => {
					if (!!data[key]) {
						result[key] = data[key];
					}
					return result;
				}, {}),
			};
		}
	} else {
		return fetch(baseUrl)
			.then((a: any) => a.json())
			.then((baseData: any) => {
				return fetch(url)
					.then((a: any) => a.json())
					.then((data: any) => {
						(global as any).langObj[lang] = {
							...baseData,
							...Object.keys(data).reduce((res: any, key: string) => {
								if (!!data[key]) {
									res[key] = data[key];
								}
								return res;
							}, {}),
						};
						return (global as any).langObj[lang];
					})
					.catch((e: any) => {
						console.log('get i18n data error.', e);
					});
			})
			.catch((e: any) => {
				console.log('get i18n data error.', e);
			});
	}
	return;
}

